package spaceSim;

public class Dot {
	private Double x, y, radius;

	public Dot(Double x, Double y, Double radius) {
		this.x = x;
		this.y = y;
		this.radius = radius;
	}
	public Double getX(){
		return x;
	}
	public Double getY(){
		return y;
	}
	public Double getRadius(){
		return radius;
	}
}
